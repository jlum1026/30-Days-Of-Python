print(2 + 3)   # addition(+)
print(3 - 1)   # subtraction(-)
print(2 * 3)   # multiplication(*)
print(3 / 2)   # division(/)
print(3 ** 2)  # exponential(**)
print(3 % 2)   # modulus(%)
print(3 // 2)  # Floor division operator(//)

# Checking data types

print(type(10))                  # Integer (whole number)
print(type(3.14))                # Float (decimal)
print(type(1 + 3j))              # Complex
print(type('Asabeneh'))          # String (words/numbers INSIDE 2 COMMAS '')
print(type([1, 2, 3]))           # List (anything inside square brackets)
print(type({'name':'Asabeneh'})) # Dictionary () dictionary['name']
print(type({9.8, 3.14, 2.7}))    # Set (x duplicate values)
print(type((9.8, 3.14, 2.7)))    # Tuple (x change)
